public class DoubleVsFloat {

    public static void main(String[] args) {

        //double jest domyślny, do float trzeba dopisać literkę F,  double zajmuje troche więcej pamięci i jest dokładniejszy i ma większy zakres.;

        double a = 3.14;
        float b = 3.124f;
        float c = 3123F;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

    }
}
