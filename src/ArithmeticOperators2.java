public class ArithmeticOperators2 {

    public static void main(String[] args) {
        double a = 2 +3;
        System.out.println("a " + a);

        double b = 10;
        double c = 3;
        double d = b + c;

        System.out.println("d "+ d);


        d = b - c;
        System.out.println(d);

        d = b * c;
        System.out.println(d);
        d = b / c;
        System.out.println(d);

        System.out.println("d: " + d);

        //modulo = reszta z dzielenia

        d = b % c;
        System.out.println(d);

    }
}
